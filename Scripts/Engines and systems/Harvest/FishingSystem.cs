        /// <summary>
        /// Initializes the fishing system with basic harvest definitions
        /// </summary>
        public Fishing()
        {
            HarvestResource[] res;
            HarvestVein[] veins;

            #region Fishing
            HarvestDefinition fish = m_Definition = new HarvestDefinition();

            // Resource banks are every 4x4 tiles
            fish.BankWidth = HarvestConstants.FISHING_BANK_WIDTH;
            fish.BankHeight = HarvestConstants.FISHING_BANK_HEIGHT;

            // Every bank holds from 1 to 9 fish
            fish.MinTotal = HarvestConstants.FISHING_BANK_MIN_TOTAL;
            fish.MaxTotal = HarvestConstants.FISHING_BANK_MAX_TOTAL;

            // A resource bank will respawn its content every 15 to 30 minutes
            fish.MinRespawn = TimeSpan.FromMinutes(HarvestConstants.FISHING_RESPAWN_MIN_MINUTES);
            fish.MaxRespawn = TimeSpan.FromMinutes(HarvestConstants.FISHING_RESPAWN_MAX_MINUTES);

            // Skill checking is done on the Fishing skill
            fish.Skill = SkillName.Fishing;

            // Set the list of harvestable tiles
            fish.Tiles = m_WaterTiles;
            fish.RangedTiles = true;

            // Players must be within 4 tiles to harvest
            fish.MaxRange = HarvestConstants.FISHING_MAX_RANGE;

            // One fish per harvest action
            fish.ConsumedPerHarvest = HarvestConstants.FISHING_CONSUMED_PER_HARVEST;
            fish.ConsumedPerFeluccaHarvest = HarvestConstants.FISHING_CONSUMED_PER_HARVEST;

            // The fishing effect
            fish.EffectActions = HarvestConstants.FISHING_EFFECT_ACTIONS;
            fish.EffectSounds = HarvestConstants.FISHING_EFFECT_SOUNDS;
            fish.EffectCounts = HarvestConstants.FISHING_EFFECT_COUNTS;
            fish.EffectDelay = TimeSpan.FromSeconds(HarvestConstants.FISHING_EFFECT_DELAY);
            fish.EffectSoundDelay = TimeSpan.FromSeconds(HarvestConstants.FISHING_EFFECT_SOUND_DELAY);

            fish.NoResourcesMessage = HarvestConstants.MSG_FISH_NOT_BITING;
            fish.FailMessage = HarvestConstants.MSG_FAILED_CATCH;
            fish.TimedOutOfRangeMessage = HarvestConstants.MSG_TOO_FAR_FROM_WATER;
            fish.OutOfRangeMessage = HarvestConstants.MSG_TOO_FAR_FROM_WATER;
            fish.PackFullMessage = HarvestConstants.MSG_BACKPACK_FULL_FISH;
            fish.ToolBrokeMessage = HarvestConstants.MSG_POLE_BROKEN;

            res = new HarvestResource[]
                {
                    new HarvestResource(HarvestConstants.FISH_SKILL_MIN, HarvestConstants.FISH_SKILL_MIN, HarvestConstants.FISH_SKILL_MAX, HarvestStringConstants.MSG_CAUGHT_COMMON_FISH, typeof(Fish))
                };

            // the sum chance Needs to be 100%
            veins = new HarvestVein[]
                {
                    new HarvestVein(HarvestConstants.FISH_VEIN_CHANCE, 0.0, res[0], null)
                };

            fish.Resources = res;
            fish.Veins = veins;

            fish.BonusResources = new BonusHarvestResource[]
            {
                new BonusHarvestResource(0, 99.0, null, null),
                new BonusHarvestResource(75.0, 1.0, "Eba! Você encontrou uma pérola negra.", typeof(BlackPearl)),
            };

            fish.RaceBonus = false;
            fish.RandomizeVeins = false;

            Definitions.Add(fish);
            #endregion
        }

        /// <summary>
        /// Gets the resource type for fishing operations
        /// </summary>
