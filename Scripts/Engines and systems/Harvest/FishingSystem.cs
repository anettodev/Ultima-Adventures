        /// <summary>
        /// Checks if a mobile can perform basic fishing
        /// </summary>
        public override bool CheckHarvest(Mobile from, Item tool)
        {
            PlayerMobile pm = from as PlayerMobile;
            if (!base.CheckHarvest(from, tool))
                return false;

            if (from.Mounted)
            {
                pm.SendMessage(55, "Não é possível pescar em cima de um cavalo.");
                return false;
            }
            else if (from.IsBodyMod && !from.Body.IsHuman)
            {
                pm.SendMessage(55, "Não é possível pescar enquanto está sob efeito de polimorfismo.");
                return false;
            }

            return true;
        }

        /// <summary>
        /// Sends success message for fishing
        /// </summary>
        public override void SendSuccessTo(Mobile from, Item item, HarvestResource resource)
        {
            base.SendSuccessTo(from, item, resource);
        }

        /// <summary>
        /// Checks harvest conditions including tool and target validation
